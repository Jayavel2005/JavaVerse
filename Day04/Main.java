public class Main {
    public static void main(String[] args) {
        // While loops

        // Initialize a counter variable to 0
        int count = 0;

        // This while loop will run as long as count is less than or equal to 20
        while (count <= 20) {
            // Print the current value of count
            System.out.println(count);

            // Increment count by 1
            count++;
        }

        // Initialize another counter variable to 20
        int i = 20;

        // This while loop will run as long as i is greater than or equal to 0
        while (i >= 0) {
            // Print the current value of i
            System.out.println(i);

            // Decrement i by 1
            i--;
        }
    }
}
